import java.util.Scanner;

public class timeloop_260883135 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int num = sc.nextInt(); // num times to print
        for (int i = 1; i <= num; i ++) {
            System.out.println(i + " Abracadabra");
        }
        sc.close();

    }
    
}
